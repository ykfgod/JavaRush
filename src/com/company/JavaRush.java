package com.company;

import java.util.Scanner;

/*
    JavaRush 4.3 Заполненный прямоугольник
 */
public class JavaRush {

    public static void main(String[] args) {
        int n = 0;

        while (n < 5) {

            int m = 0 ;

            while (m < 10){
                System.out.print('Q');
                m++;
            }

            n++;
            System.out.println();
        }
    }
}